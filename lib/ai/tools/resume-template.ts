/**
 * 简历模板 Tool
 *
 * 当用户需要查看简历模板或不知道如何写简历时，
 * AI 会自动调用此 tool 获取标准的程序员简历模板。
 *
 * 这个 tool 返回的是一个固定的简历模板字符串，
 * AI 不需要自己编写简历模板，直接使用 tool 返回的内容即可。
 */

import { tool } from "ai";
import { z } from "zod";

/**
 * 获取简历模板的异步函数
 * 实际项目中可以改为从数据库或远程 API 获取
 * 这里直接返回一个预设的模板字符串
 */
function getResumeTemplate() {
  return `

    # 程序员简历模板
    
    标题：姓名-电话-岗位
  
    ## 个人信息
    - 包括：姓名，性别，电话，邮箱，城市，学历
    - 年龄非必需，可根据毕业时间可推算出来
    - 照片非必需，如果是女生可以考虑加上照片（大部分面试官都是男的，女生有照片会多看一眼，多个机会）
  
    ## 教育经历
    - 包括：学校，专业，学历，毕业时间，获奖
    - 如果是大专或专升本，把教育经历放在最后
    
    ## 个人优势
    - 总结 2-3 条你相比于同龄人、身边同事的优势，尽量写技术相关的、写的客观一点，不要太主观

    ## 专业技能
    - 体现你的技术广度、深度，毕业时间越久，就要写的越丰富
    - 不要写"了解 xxx 技术"，要么写"熟悉 xxx 技术"，要么不写
    - 毕业时间久的，不要写太基础的技能，这样会显得自己技术视野太低
    
    ## 工作/实习 经历
    - 包括：公司，职位，时间，工作内容，工作成果

    ## 项目经验
    - 包括：项目名称，时间，项目描述，项目职责，项目成果
  `;
}

/**
 * 简历模板 tool 定义
 *
 * 使用 AI SDK 的 tool 函数定义，包含：
 * - description: 告诉 AI 什么时候应该调用这个 tool
 * - inputSchema: 输入参数的 zod schema（这里不需要参数，传空对象）
 * - execute: 实际执行的异步函数
 */
export const getResumeTemplateTool = tool({
  description:
    "获取程序员简历模板。当用户需要查看简历模板或不知道如何写简历时，可以使用此工具获取标准的简历模板格式。",
  inputSchema: z.object({}),
  execute: async () => {
    const template = await getResumeTemplate();
    return {
      template,
    };
  },
});
